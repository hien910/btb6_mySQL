-- 2. Để tính giá trị trung bình của một cột trong MySQL, bạn sử dụng hàm nào?
-- c) AVG

-- 3. Hàm MAX trong MySQL được sử dụng để làm gì?
-- c) Trả về giá trị lớn nhất trong cột

-- 4. Khi bạn muốn tìm giá trị nhỏ nhất trong một cột cụ thể trong MySQL, bạn sử dụng hàm nào?
